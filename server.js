      const { data: updatedProfile, error: updateError } = await supabase
        .from("user_profiles")
        .update({ 
          updated_at: new Date().toISOString(),
          name: userInfo.name,
          picture: userInfo.picture
        })
        .eq("id", existingProfile.id)
        .select()
        .single();
        
